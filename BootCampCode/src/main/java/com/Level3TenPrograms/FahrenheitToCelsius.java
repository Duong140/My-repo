package com.Level3TenPrograms;

public class FahrenheitToCelsius {
	

    public static void main(String[] args)
    {
        
        double fahrenheit = 50.0, celsius = 0.0;
 
       
        celsius = (fahrenheit - 32) / 1.8;
 
     
        System.out.println(
            "value of temperature in celsius:" + celsius);
    }
	
}
