package com.javacode.day3;

public class pattern2 {

	public static void main(String[] args) {
		for(int i=1 ; i<=5 ; i++) {
			for(int q=1 ; q<=i ; q++) {
			System.out.print("* ");
		}
		System.out.println();	
	}
		for(int p=1 ; p>=5; p++) {
			for(int u=4 ; u<=p ; u--) {
				System.out.print("* ");
			}
			System.out.println();
		}

  }
}
